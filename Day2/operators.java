public class operators {
    public static void main(String[] args) {
        int a=7, b = 11;
        System.out.println("a is " +a+ " b is "+b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
